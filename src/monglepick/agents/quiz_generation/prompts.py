"""
퀴즈 생성 에이전트 LLM 프롬프트 모음.

질문 카테고리 5종(genre / director / year / cast / plot)과
난이도 3종(easy / medium / hard)을 조합하여 영화당 1문항씩 생성한다.

운영 원칙:
    - 스포일러 절대 금지 (결말/반전/사망 등 핵심 전개 노출 불가)
    - 객관식 정확히 4지선다, 정답은 options 중 하나와 100% 일치
    - JSON 객체만 출력 (마크다운/설명 문구 금지)
"""

from __future__ import annotations


# ============================================================
# 시스템 프롬프트 (모든 카테고리 공통)
# ============================================================

QUIZ_SYSTEM_PROMPT = """당신은 영화 교육 퀴즈 전문가입니다.
주어진 영화 한 편에 대해 객관식 4지선다 퀴즈 1문항을 생성하세요.

반환 형식(JSON 객체만 출력, 설명/마크다운 금지):
{{
  "question": "질문 텍스트",
  "options": ["선택지1", "선택지2", "선택지3", "선택지4"],
  "correctAnswer": "정답 선택지(options 배열 중 하나와 정확히 일치)",
  "explanation": "정답 해설(1~2문장)",
  "category": "{category}"
}}

규칙:
- 스포일러 절대 금지 (결말/반전/사망/주요 인물 운명 등 노출 불가)
- 객관식 선택지는 반드시 4개, 모두 서로 다른 문자열
- 오답 선택지는 그럴듯하되 사실과 다르게 구성
- correctAnswer 는 options 배열에 존재하는 문자열과 100% 동일해야 함
- 난이도 '{difficulty}' 수준으로 맞출 것 (easy=상식, medium=영화 팬 수준, hard=마니아)
- 카테고리 '{category}' 에 해당하는 질문 형식으로 작성
- 순수 JSON 객체만 출력 (마크다운 코드블록·설명 문구 금지)"""


# ============================================================
# 카테고리별 사용자 프롬프트 가이드
# ============================================================
#
# question_generator 노드가 영화 메타데이터(title/genres/director/cast/...) 와
# 카테고리에 맞춰 user_prompt 를 조립한다. 카테고리는 라운드로빈으로 순환하여
# 한 번의 배치 생성 결과가 동일 패턴으로 쏠리는 것을 방지한다.

CATEGORY_GUIDES: dict[str, str] = {
    # 장르: 가장 안전 — 메타가 부족해도 항상 답을 만들 수 있다.
    "genre": (
        "이 영화의 주요 '장르'를 묻는 퀴즈를 만드세요. "
        "오답은 그럴듯하지만 영화의 실제 장르가 아닌 다른 장르를 선택하세요."
    ),
    # 감독: 감독 정보가 비어 있으면 fallback 으로 강등된다.
    "director": (
        "이 영화의 '감독'을 묻는 퀴즈를 만드세요. "
        "오답은 같은 시대·국적·장르의 다른 감독 이름을 선택하세요."
    ),
    # 개봉 연도: 연도는 ±2 / ±5 / ±10 단위로 오답 분산
    "year": (
        "이 영화의 '개봉 연도'를 묻는 퀴즈를 만드세요. "
        "오답은 정답 연도에서 2~10년 떨어진 그럴듯한 다른 연도들로 구성하세요."
    ),
    # 출연진: 주연/조연 이름 식별
    "cast": (
        "이 영화의 '주연 배우'를 묻는 퀴즈를 만드세요. "
        "오답은 같은 시대·장르의 다른 배우 이름을 선택하되, 실제로는 이 영화에 출연하지 않은 인물을 사용하세요."
    ),
    # 줄거리/배경: overview 가 있을 때만 활성화
    "plot": (
        "주어진 줄거리(overview)를 꼼꼼히 읽고 이 영화만의 독특한 배경·설정·사건을 묻는 퀴즈를 만드세요. "
        "도입부·중반부의 구체적 장소·시대·직업·사건을 다루되, 결말·반전·등장인물의 운명은 절대 포함하지 마세요. "
        "오답은 비슷한 장르 다른 영화의 설정에서 그럴듯하게 가져오세요."
    ),
    # 일반: 카테고리 매칭 실패 시 디폴트
    "general": (
        "이 영화에 대한 '제작 비하인드 또는 대표적 특징'을 묻는 퀴즈를 만드세요. "
        "스포일러를 제외한 일반 사실 중심으로 4지선다를 구성하세요."
    ),
}


# ============================================================
# 라운드로빈 카테고리 순환 순서
# ============================================================
#
# question_generator 가 영화 인덱스 % len(CATEGORY_ROTATION) 으로
# 카테고리를 선택한다. 메타가 비어있는 카테고리는 메타 체크 단계에서
# "general" 로 다운그레이드된다.
CATEGORY_ROTATION: list[str] = ["genre", "director", "year", "cast", "plot"]


# ============================================================
# 스포일러 키워드 블랙리스트 (quality_validator 사용)
# ============================================================
#
# 한국어 스포일러성 어휘 — 결말/반전/사망 직결 단어들. 한 단어라도 question
# 또는 explanation 에 포함되면 quality_validator 가 valid=False 처리한다.
SPOILER_BLACKLIST: list[str] = [
    "결말",
    "엔딩",
    "마지막에",
    "마지막 장면",
    "사망",
    "죽는다",
    "죽는 장면",
    "죽임",
    "반전",
    "트위스트",
    "범인은",
    "진범",
    "정체는",
    "최종 보스",
    "비밀이 밝혀",
]


def build_user_prompt(
    title: str,
    genres: list[str],
    release_year: str,
    director: str,
    cast_members: list[str],
    overview: str,
    tagline: str,
    keywords: list[str],
    category: str,
) -> str:
    """
    영화 메타데이터 + 카테고리 가이드를 결합해 LLM 사용자 프롬프트를 조립한다.

    빈 필드는 "정보 없음" 으로 채워 LLM 이 환각하지 않도록 한다.
    overview 는 600자로 truncate 하여 토큰 사용량을 제한한다.
    """
    guide = CATEGORY_GUIDES.get(category, CATEGORY_GUIDES["general"])
    genres_str = ", ".join(genres) if genres else "정보 없음"
    cast_str = ", ".join(cast_members[:5]) if cast_members else "정보 없음"
    keywords_str = ", ".join(keywords[:10]) if keywords else "정보 없음"
    overview_short = (overview or "정보 없음")[:600]

    return (
        f"[카테고리 지시]\n{guide}\n\n"
        f"[영화 정보]\n"
        f"- 제목: {title}\n"
        f"- 장르: {genres_str}\n"
        f"- 개봉연도: {release_year or '미상'}\n"
        f"- 감독: {director or '정보 없음'}\n"
        f"- 출연: {cast_str}\n"
        f"- 키워드: {keywords_str}\n"
        f"- 태그라인: {tagline or '정보 없음'}\n"
        f"- 줄거리: {overview_short}\n\n"
        f"위 규칙을 모두 지키고 JSON 객체로만 응답하세요."
    )
