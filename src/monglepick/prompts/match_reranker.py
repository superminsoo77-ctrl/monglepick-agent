"""
Movie Match v3 LLM 리랭커 프롬프트 (Solar API).

두 영화 A/B 와 후보 리스트를 주고, "이 두 영화를 모두 좋아하는 사람의 관점" 에서
각 후보가 얼마나 잘 맞는지 0~10점 점수 + 한줄 이유를 JSON 으로 반환하게 한다.

### 설계 원칙
- `/prompts/rerank.py` (채팅 리랭커) 의 구조를 준용하되, "사용자 요청" 대신
  "두 영화 동시 선호 프로필" 을 평가 기준으로 함.
- 교집합 특성에만 의존하지 않고 **두 영화의 매력을 조합 시너지** 로 판단하도록 유도.
- 국가/언어 필터는 Match 에 약함(두 영화의 교집합이 이미 반영됨) — 채팅처럼 "0점 강제" 하지 않음.
"""

